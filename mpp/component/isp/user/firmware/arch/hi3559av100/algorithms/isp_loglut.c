static HI_VOID LogLUTRegsInitialize(VI_PIPE ViPipe, ISP_REG_CFG_S *pstRegCfg)
{
    HI_U32 i, j;
    HI_U8  u8BlockNum = 0;
    ISP_CTX_S *pstIspCtx        = HI_NULL;
    ISP_CMOS_DEFAULT_S *pstSnsDft = HI_NULL;
    ISP_LOGLUT_DYNA_CFG_S   *pstDynaRegCfg   = HI_NULL;
    ISP_LOGLUT_STATIC_CFG_S *pstStaticRegCfg = HI_NULL;

    ISP_SensorGetDefault(ViPipe, &pstSnsDft);
    ISP_GET_CTX(ViPipe, pstIspCtx);

    u8BlockNum = pstIspCtx->stBlockAttr.u8BlockNum;
    for (i = 0; i < u8BlockNum; i++)
    {
        pstStaticRegCfg = &pstRegCfg->stAlgRegCfg[i].stLogLUTRegCfg.stStaticRegCfg;
        pstDynaRegCfg   = &pstRegCfg->stAlgRegCfg[i].stLogLUTRegCfg.stDynaRegCfg;
        //static reg init
        pstDynaRegCfg->bEnable = HI_FALSE;
        pstStaticRegCfg->u8BitDepthOut = 10;

        if (pstSnsDft->stLogLUT.bValid)
        {
            for (j = 0; j < LOG_LUT_SIZE; j++)
            {
                pstStaticRegCfg->au32LogLUT[j] = pstSnsDft->stLogLUT.au32LogLUT[j];
            }
        }
        else
        {
            for (j = 0; j < LOG_LUT_SIZE; j++)
            {
                pstStaticRegCfg->au32LogLUT[j] = LogLUT[j];
            }
        }
        pstStaticRegCfg->bStaticResh = HI_TRUE;
    }
    pstRegCfg->unKey.bit1LogLUTCfg = 1;

    return;
}

static HI_S32 LogLUTReadExtregs(VI_PIPE ViPipe)
{
    ISP_LOGLUT_S *pstLogLUT = HI_NULL;

    LOGLUT_GET_CTX(ViPipe, pstLogLUT);

    pstLogLUT->bEnable = hi_ext_system_loglut_enable_read(ViPipe);

    return HI_SUCCESS;
}

HI_S32 LogLUTProcWrite(VI_PIPE ViPipe, ISP_CTRL_PROC_WRITE_S *pstProc)
{
    ISP_CTRL_PROC_WRITE_S stProcTmp;
    ISP_LOGLUT_S *pstLogLUT = HI_NULL;

    LOGLUT_GET_CTX(ViPipe, pstLogLUT);

    if ((HI_NULL == pstProc->pcProcBuff)
        || (0 == pstProc->u32BuffLen))
    {
        return HI_FAILURE;
    }

    stProcTmp.pcProcBuff = pstProc->pcProcBuff;
    stProcTmp.u32BuffLen = pstProc->u32BuffLen;

    ISP_PROC_PRINTF(&stProcTmp, pstProc->u32WriteLen,
                    "-----LogLUT INFO--------------------------------------------------------------\n");

    ISP_PROC_PRINTF(&stProcTmp, pstProc->u32WriteLen, "%16s\n", "Enable");

    ISP_PROC_PRINTF(&stProcTmp, pstProc->u32WriteLen, "%16u\n", pstLogLUT->bEnable);

    pstProc->u32WriteLen += 1;

    return HI_SUCCESS;
}

